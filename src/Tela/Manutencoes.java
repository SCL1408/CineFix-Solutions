    private void jbRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRemoverActionPerformed
        if (tbProjetores.getSelectedRow() != -1) {
            DefaultTableModel tbcadastro = (DefaultTableModel) tbProjetores.getModel();
            tbcadastro.removeRow((tbProjetores.getSelectedRow()));
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cadastro para excluir!");
        }
    }//GEN-LAST:event_jbRemoverActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        //verifica se há coluna selecionada
        if (tbProjetores.getSelectedRow() != -1) {
            tbProjetores.setValueAt(jtIdManutencao.getText(), tbProjetores.getSelectedRow(), 0);
            tbProjetores.setValueAt(jcbProjetor.getSelectedItem(), tbProjetores.getSelectedRow(), 1);
            tbProjetores.setValueAt(jcbTecnico.getSelectedItem(), tbProjetores.getSelectedRow(), 2);
            tbProjetores.setValueAt(jtDataManutencao.getText(), tbProjetores.getSelectedRow(), 3);
            limparCampos();
            tbProjetores.setSelectionMode(-1);//deseleciona a linha selecionada
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cadastro para Alterar!");
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        DefaultTableModel tbcadastro = (DefaultTableModel) tbProjetores.getModel();
        Object[] dados = {
            contador++,
            jcbProjetor.getSelectedItem(),
            jcbTecnico.getSelectedItem(),
            jtDataManutencao.getText()
        };
        tbcadastro.addRow(dados);
        limparCampos();
    }//GEN-LAST:event_jbCadastrarActionPerformed

