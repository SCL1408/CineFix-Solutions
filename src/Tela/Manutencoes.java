    private void tbProjetoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProjetoresMouseClicked
        if (tbProjetores.getSelectedRow() != -1) {
            jtIdManutencao.setText(tbProjetores.getValueAt(tbProjetores.getSelectedRow(), 0).toString());
            jcbProjetor.setSelectedItem(tbProjetores.getValueAt(tbProjetores.getSelectedRow(), 1).toString());
            jcbTecnico.setSelectedItem(tbProjetores.getValueAt(tbProjetores.getSelectedRow(), 2).toString());
            jtDataManutencao.setText(tbProjetores.getValueAt(tbProjetores.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tbProjetoresMouseClicked

